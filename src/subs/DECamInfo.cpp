Bounds<int> decam::datasec(const string detpos, const string amp) {
    if ((detpos[0] == 'S' && amp == "A") || (detpos[0] == 'N' && amp == "B")) {
        return Bounds<int>(1025, 2048, 1, 4096);
    } else if ((detpos[0] == 'S' && amp == "B") || (detpos[0] == 'N' && amp == "A")) {
        return Bounds<int>(1, 1024, 1, 4096);
    } else {
        throw std::runtime_error("datasec() has bad detpos/amp combination " + detpos + "/" + amp);
    }
}

std::map<string, Device> decam::decamInfo() {
    std::map<string, Device> info;
    {
        std::istringstream iss(ccdData);
        string buffer;
        DECamMap map;
        while (getlineNoComment(iss, buffer)) {
            std::istringstream iss(buffer);
            string name;
            Device d;
            if (!(iss >> name >> d.ccdnum >> d.detsecX >> d.detsecY)) {
                std::cerr << "Bad Device line: " << buffer << std::endl;
                exit(1);
            }
            map.setDevice(name);
            Bounds<double> b;
            double x, y;
            map.toField(1., 1., x, y);
            b += Position<double>(x, y);
            map.toField(1., 2048., x, y);
            b += Position<double>(x, y);
            map.toField(1., 4096., x, y);
            b += Position<double>(x, y);
            map.toField(1024., 4096., x, y);
            b += Position<double>(x, y);
            map.toField(2048., 4096., x, y);
            b += Position<double>(x, y);
            map.toField(2048., 2048., x, y);
            b += Position<double>(x, y);
            map.toField(2048., 1., x, y);
            b += Position<double>(x, y);
            map.toField(1024., 1., x, y);
            b += Position<double>(x, y);
            d.b = b;
            d.norm = 1.;
            info[name] = d;
        }
    }
    return info;
}

void decam::getDeviceNorms(string filename, std::map<string, Device> &devices) {
    std::ifstream ifs(filename.c_str());
    if (!ifs) {
        std::cerr << "could not open device norms file " << filename << std::endl;
        exit(1);
    }
    string buffer;
    while (getlineNoComment(ifs, buffer)) {
        std::istringstream iss(buffer);
        string detpos;
        double norm;
        if (!(iss >> detpos >> norm)) {
            std::cerr << "Bad input line in device norm file: <" << buffer << ">" << std::endl;
            exit(1);
        }
        if (devices.count(detpos) == 0) {
            std::cerr << "Unknown DETPOS in norm file: <" << detpos << ">" << std::endl;
            exit(1);
        }
        devices[detpos].norm = norm;
    }
}

DECamMap::DECamMap() : prefix("i/"), pixmap(nullptr) {
    std::istringstream iss(referenceMap);
    pmc.read(iss);

    // Shift all field coordinates so that center of N4 & S4 is origin:
    Bounds<double> b;
    double x, y;
    pmc.issueMap(prefix + "N4")->toWorld(1024., 2048., x, y);
    b += Position<double>(x, y);
    pmc.issueMap(prefix + "S4")->toWorld(1024., 2048., x, y);
    b += Position<double>(x, y);
    centerX = b.center().x;
    centerY = b.center().y;
}

void DECamMap::setDevice(string detpos) { pixmap = pmc.issueMap(prefix + detpos); }

void DECamMap::toPix(double xField, double yField, double &xPix, double &yPix) const {
    pixmap->toPix(xField + centerX, yField + centerY, xPix, yPix);
    return;
}

void DECamMap::toField(double xPix, double yPix, double &xField, double &yField) const {
    pixmap->toWorld(xPix, yPix, xField, yField);
    xField -= centerX;
    yField -= centerY;
    return;
}

